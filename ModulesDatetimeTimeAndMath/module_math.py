import math

x = 3.7

ceil_result = math.ceil(x)
floor_result = math.floor(x)
trunc_result = math.trunc(x)

print(f"Ceil: {ceil_result}")
print(f"Floor: {floor_result}")
print(f"Trunc: {trunc_result} \n")



print(math.pi)

angle = math.radians(3.14)
print(f"Radius: {angle}")

num = 22

print(f"The square root of {num} = {math.sqrt(num)}")

print(f"Log: {math.log(10, 2)}")